<b>What We Have Done: </b>
<br>
<br>
We have created and presented a simple and <u>unique universal peace plan</u> that can be utilized by any one country and then followed by others. It's almost like what we are doing now, but not quite. In essence, the plan divorces military action from diplomacy. It provides protection from outside attack by other nations but does not allow one nation a chance to attack another. It basically creates "More Defense at Less Expense."
<br>
<br>
We believe the plan will reduce the desire for and incidences of terrorism. Moreover, we have in mind certain modifications and additions that will help prevent civil strife and civil war.
<br>
<br>
We have <u>presented the plan to many world leaders,</u> and in several cases we have received meaningful responses, but it is a little too radical to expect a world leader to embrace the plan wholeheartedly without substantial public clamor for such a new idea. And that is why we formed World Peace Society - to educate people to this idea and create discussion and debate about its ramifications.
<br>
<br>
We have <u>presented the plan at a meeting of the United Nations,</u> and it received some interest.  Ambassador Miljana Komatina, - who was representative to the UN from the former country of Yugoslavia and later became Undersecretary General of the UN, and at one point Chief of Disarmament Negotiations at the UN - read our plan and agreed to join our Board of Directors. But unfortunately he died.
<br>
<br>
We believe this peace plan, "World Peace Now and Forever," is the most meaningful effort that has ever been advanced to prevent war and create a more peaceful world. We welcome anyone to put forward a better plan or to offer suggestions and ideas that we can incorporate to make our plan more effective. In fact, we will <u>support efforts to improve our ideas. </u>
<br>
<br>
The poem, "Ode to a Better World," in its first three parts states why such a plan is necessary, and in its last three parts describes the nature of the plan, why and how it should be put in place.
<br>
<br>
In the poem, "A Happier Place," and in the song, "World Peace Now and Forever," are references to the plan. All are on this site in the sections, "Send a Poem to a Friend or Contact" and "Send a Song to a Friend or Contact." Those who wish to view our essay and specific plan, "World Peace Now and Forever," in its original written form, may receive a copy by mailing us a check for $50.
<br>
<br>
In the area of <u>education,</u> we have written and recorded a song that we think can have profound implications on our educational system and the development of our population. In a simple song, we have clearly and completely expressed the ten basic values that make a person a worthwhile human being. We wrote it as a popular ballad for all people, but then we realized its greatest application is as a means of <u>teaching character values to our young from the age of 2 to 12.</u> We want it used by every parent and school in the world to better bring up children. Look at "The Essence of Humanity Credo&trade;" and the song, "An Outstanding Me," which is on this site under Songs. Our many other poems and songs on peace, tolerance, drug abuse and in general a better world can also be taught to children to broaden their education.
<br>
<br>
There exist fundamental facts about almost all human beings that we can assume as a given truth, because we have seen the evidence over thousands of years, among billions of people, and in every land and culture on this globe:
<br>
<br>
<ul>
1) Almost all people learn some form of language. Some poorly, some well. But almost all can communicate at some level.
<br>
<br>
2) Almost all people are toilet trained.
<br>
<br>
3) Almost all people believe in some form of god.
</ul>
People are not born with these abilities. They learn these rather complicated tasks as youngsters. If we can teach almost every human being on this globe such involved procedures as these, why can't we teach them ten basic values that will make them better human beings and create a more worthwhile civilization for all.
<br>
<br>
If one had the first five values in his soul, one could not be any form of criminal, Nazi, or terrorist. And any improvement to our civilization since the beginning of time had to involve someone somewhere practicing one of the second five values. That's why we call our values the "Essence of Humanity Credo&trade;."
<br>
<br>
We have created three <u>anti-drug songs.</u> If one seriously listened to these song lyrics, they certainly would hesitate before trying harmful drugs and maybe avoid the habit altogether. If hooked on the habit, hearing these songs repeatedly may even cure them.
<br>
<br>
Some of our songs may help deter one from following criminal behavior; and our songs and poems on a <u>more prosperous economy</u> and a more effective way to work may lead many to <u>earn greater income.</u> We have some plans that may help reduce recidivism, but we will need a faster growing "World Peace Society" to activate them.
<br>
<br>
